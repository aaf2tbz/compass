export type FileType =
  | "folder"
  | "document"
  | "spreadsheet"
  | "image"
  | "video"
  | "pdf"
  | "code"
  | "archive"
  | "audio"
  | "unknown"

export type SharedRole = "viewer" | "editor"

export type SharedUser = {
  name: string
  avatar?: string
  role: SharedRole
}

export type FileOwner = {
  name: string
  avatar?: string
}

export type FileItem = {
  id: string
  name: string
  type: FileType
  mimeType?: string
  size: number
  path: string[]
  createdAt: string
  modifiedAt: string
  owner: FileOwner
  starred: boolean
  shared: boolean
  sharedWith?: SharedUser[]
  trashed: boolean
  parentId: string | null
}

